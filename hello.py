# checking data types

print(type('this 30 days will have both tears and laughter')) #string
print(type(5)) # integer
print(type(True)) # bool
print(type(1 + 6j)) # complex
print(type(0.09)) # decimal
print(type(['a','b','c'])) # list
print(type({'a','b','c'})) # set
print(type({'a':1,'b':2,'c':3})) # dictionary
print(type(('a','b','c'))) # tuple



